var nilaiP = 16;
var nilaiQ = 4;
var hasil = 0;
//penjumlahan
hasil = nilaiP + nilaiQ
document.write(`${nilaiP} + ${nilaiQ} = ${hasil}<br/>`);
 // pengurangan
 hasil = nilaiP - nilaiQ
document.write(`${nilaiP} - ${nilaiQ} = ${hasil}<br/>`);
 // Perkalian
 hasil = nilaiP * nilaiQ
document.write(`${nilaiP} * ${nilaiQ} = ${hasil}<br/>`);
 // pemangkatan = a ** b;
 hasil = nilaiP ** nilaiQ
document.write(`${nilaiP} ** ${nilaiQ} = ${hasil}<br/>`);
 // Pembagian
 hasil = nilaiP / nilaiQ
document.write(`${nilaiP} / ${nilaiQ} = ${hasil}<br/>`);
 // Modulo = sisa bagi
 hasil = nilaiP % nilaiQ
 document.write(`${nilaiP} % ${nilaiQ} = ${hasil}<br/>`);