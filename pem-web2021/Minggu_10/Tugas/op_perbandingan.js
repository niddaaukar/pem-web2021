var nilaiA = 12;
 var nilaiB = 10;
 // sama dengan
 var hasil = nilaiA == nilaiB;
 document.write(`${nilaiA} == ${nilaiB} = ${hasil}<br/>`);
 
 // lebih besar
 var hasil = nilaiA > nilaiB;
 document.write(`${nilaiA} > ${nilaiB} = ${hasil}<br/>`);
 
 // lebih besar samadengan
 var hasil = nilaiA >= nilaiB;
 document.write(`${nilaiA} >= ${nilaiB} = ${hasil}<br/>`);
 
 // lebih kecil
 var hasil = nilaiA < nilaiB;
 document.write(`${nilaiA} < ${nilaiB} = ${hasil}<br/>`);
 
 // lebih kecil samadengan
 var hasil = nilaiA <= nilaiB;
 document.write(`${nilaiA} <= ${nilaiB} = ${hasil}<br/>`);
 
 // tidak samadengan
 var hasil = nilaiA != nilaiB;
 document.write(`${nilaiA} != ${nilaiB} = ${hasil}<br/>`);

 // sama dengan dua = hanya membandingkan nilai
 var nilaiC ="12"
 var hasil = nilaiA == nilaiC;
 document.write(`${nilaiA} == ${nilaiC} = ${hasil}<br/>`);

 // sama dengan tiga digunakan untuk membandingkan nilai dan tipe data
